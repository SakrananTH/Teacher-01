import * as XLSX from 'xlsx';

/**
 * ฟังก์ชันสำหรับส่งออกข้อมูลเป็นไฟล์ Excel พร้อมเพิ่มช่องเซ็นชื่อด้านล่าง
 * @param data Array ของ Object ที่ต้องการแปลงเป็นตาราง
 * @param filename ชื่อไฟล์ที่ต้องการบันทึก (ไม่ต้องใส่ .xlsx)
 */
export const exportToExcel = (data: any[], filename: string) => {
  // 1. แปลงข้อมูล JSON เป็น Worksheet
  const ws = XLSX.utils.json_to_sheet(data);

  // 2. คำนวณหาช่องว่างเพื่อผลักลายเซ็นไปทางขวา (เว้นไว้ประมาณ 3 คอลัมน์)
  const spacer = ['', '', ''];

  // 3. สร้างแถวลายเซ็น
  const signatureRows = [
    [], // เว้นบรรทัด
    [],
    [...spacer, '(ลงชื่อ)....................................................... ครูประจำชั้น'],
    [...spacer, '       (.......................................................)'],
    [...spacer, '        ตำแหน่ง..................................................'],
    [], // เว้นบรรทัด
    [],
    [...spacer, '(ลงชื่อ)....................................................... ผู้อำนวยการโรงเรียน'],
    [...spacer, '       (.......................................................)']
  ];

  // 4. นำแถวลายเซ็นไปต่อท้ายข้อมูลใน Worksheet (origin: -1 หมายถึงต่อท้ายสุด)
  XLSX.utils.sheet_add_aoa(ws, signatureRows, { origin: -1 });

  // 5. ปรับความกว้างของคอลัมน์อัตโนมัติ (คร่าวๆ)
  ws['!cols'] = [{ wch: 15 }, { wch: 10 }, { wch: 25 }, { wch: 15 }, { wch: 15 }, { wch: 40 }];

  // 6. สร้าง Workbook และบันทึกไฟล์
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, "รายงานสรุป");
  XLSX.writeFile(wb, `${filename}.xlsx`);
};